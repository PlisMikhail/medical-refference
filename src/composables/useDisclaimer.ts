import { computed, ref } from 'vue'
import type { ComputedRef } from 'vue'

/**
 * Подтверждение дисклеймера (T028, FR-009, SC-006).
 *
 * ЧТО ЭТО ДЕЛАЕТ: помнит, что врач уже подтвердил дисклеймер, между запусками
 * приложения. Единственное хранилище — `localStorage`, ключ
 * `med-helper:disclaimer-accepted`, значение — ISO-дата подтверждения
 * (data-model, «Клиентские состояния»).
 *
 * ЧЕГО ЭТО НЕ ДЕЛАЕТ (осознанно):
 * - НИКАКОЙ сети и никакой телеметрии: факт подтверждения не покидает
 *   устройство (конституция IV и VI);
 * - никаких других ключей, cookie, IndexedDB и sessionStorage;
 * - не хранит ничего, кроме даты: ни версии текста, ни счётчиков показов —
 *   этого не требует ни спека, ни модель данных (YAGNI).
 *
 * УСТОЙЧИВОСТЬ ХРАНИЛИЩА (требование T028). `localStorage` — не гарантия:
 * приватный режим, отключённые сайту данные, переполненная квота, WebView
 * Capacitor с урезанным storage. Обращение может как вернуть null, так и
 * БРОСИТЬ — причём даже само чтение свойства `globalThis.localStorage`
 * (Chrome при заблокированных cookie кидает SecurityError на доступе к нему).
 * Поэтому здесь три уровня защиты, и все три ведут к безопасному поведению:
 *
 *  1. доступ к самому объекту хранилища — в try/catch → нет хранилища;
 *  2. ЧТЕНИЕ упало или дало мусор → считаем «не подтверждено» и ПОКАЗЫВАЕМ
 *     дисклеймер. Безопасный дефолт — показать лишний раз, а не пропустить:
 *     непоказанный дисклеймер нарушает FR-009, лишний показ — только неудобство;
 *  3. ЗАПИСЬ упала → подтверждение всё равно вступает в силу В ПАМЯТИ на
 *     текущую сессию. Гейт не имеет права стать глухой стеной: врач подтвердил
 *     — врач заходит. Цена — на следующем запуске дисклеймер покажется снова;
 *     это честно: мы не смогли записать согласие, значит не вправе считать его
 *     записанным (то же поведение, что после очистки данных браузера — прямо
 *     допущено спекой).
 *
 * Ни один путь не бросает наружу: вызывающему коду обрабатывать нечего.
 */

/** Ключ хранения (data-model). Экспортируется для тестов — больше нигде не дублируется. */
export const DISCLAIMER_STORAGE_KEY = 'med-helper:disclaimer-accepted'

export interface Disclaimer {
  /** Подтверждён ли дисклеймер: `false` → показываем гейт. */
  accepted: ComputedRef<boolean>
  /** Явное подтверждение врачом: открывает сессию и пытается его сохранить. */
  accept(): void
}

/** Хранилище или `null`, если оно недоступно/бросает при обращении. */
function storage(): Storage | null {
  try {
    return globalThis.localStorage ?? null
  } catch {
    return null
  }
}

/**
 * ISO-дата подтверждения или `null`. Непарсящееся значение (мусор, ручная
 * правка, формат из будущей версии) трактуется как отсутствие подтверждения —
 * тот же безопасный дефолт, что и при недоступном хранилище.
 */
function readAcceptedAt(): string | null {
  let raw: string | null = null
  try {
    raw = storage()?.getItem(DISCLAIMER_STORAGE_KEY) ?? null
  } catch {
    return null
  }

  if (raw === null) return null
  return Number.isNaN(Date.parse(raw)) ? null : raw
}

/** Пытается сохранить дату подтверждения. Неуспех — не ошибка, а известный сценарий. */
function writeAcceptedAt(isoDate: string): void {
  try {
    storage()?.setItem(DISCLAIMER_STORAGE_KEY, isoDate)
  } catch {
    // Приватный режим / квота / запрет storage: см. пункт 3 в шапке файла.
    // Сессия уже открыта в памяти, ронять приложение здесь нечем и незачем.
  }
}

/**
 * Состояние дисклеймера для текущего экземпляра приложения.
 *
 * Состояние НЕ модульное, а создаётся на вызов: хранилище читается в момент
 * монтирования владельца (App.vue), а не при импорте модуля. Так гейт видит
 * актуальные данные при каждом запуске приложения, а тесты не зависят от
 * порядка выполнения. Единственный потребитель — App.vue, который раздаёт
 * результат вниз пропсами/событиями, поэтому общий синглтон не нужен.
 */
export function useDisclaimer(): Disclaimer {
  const acceptedAt = ref<string | null>(readAcceptedAt())

  function accept(): void {
    const now = new Date().toISOString()
    // Порядок важен: сначала открываем сессию, потом пробуем записать.
    // Даже если запись бросит — она это делает внутри себя — врач уже прошёл.
    acceptedAt.value = now
    writeAcceptedAt(now)
  }

  return {
    accepted: computed(() => acceptedAt.value !== null),
    accept,
  }
}
